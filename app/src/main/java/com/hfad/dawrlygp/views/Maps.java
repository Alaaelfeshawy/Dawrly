    public void getStringAddress(Double lat, Double lon) {

        Log.v("MapsLocation"," getAddress function Lat "+lat +"\n"+"Lon "+lon);

        Geocoder geocoder;
        List<Address> addresses;
        geocoder = new Geocoder(this, Locale.getDefault());

        try {
            addresses = geocoder.getFromLocation(lat, lon, 1);

            address = addresses.get(0).getAddressLine(0);

            city = addresses.get(0).getLocality();

            String state = addresses.get(0).getAdminArea();
            String country = addresses.get(0).getCountryName();

            Log.v("MapsLocation","Address "+address +"\n"+"City "+city+
                    "Address "+state +"\n"+"country "+country);

        } catch (IOException e) {
            Log.v("MapsLocation","Exception "+e.getMessage());
        }




    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        switch (item.getItemId()) {
            case android.R.id.home:
                onBackPressed();
                return true;
        }

        return super.onOptionsItemSelected(item);
    }
